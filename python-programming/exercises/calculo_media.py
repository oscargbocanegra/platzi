# Calculate la media de tres numeric pedidos por teclado.

numeric_1 = int(input('Ingrese el numero 1: '))
numeric_2 = int(input('Ingrese el numero 2: '))
numeric_3 = int(input('Ingrese el numero 3: '))

media = (numeric_1 + numeric_2 + numeric_3) / 3

print ('La media de los numeros es :' + str(media))